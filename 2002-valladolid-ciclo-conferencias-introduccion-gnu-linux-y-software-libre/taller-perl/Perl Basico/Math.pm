# Modulo de funciones matematicas no usuales


package Math;

#-----------------------------------------------------------------------------

sub max
{
   $maximo = $_[0];
   for($i=1; $i<$#_; $i++)
   {
      if($_[$i] > $maximo) { $maximo = $_[$i]; }
   }
   return $maximo
}

#-----------------------------------------------------------------------------

sub min
{
   $minimo = $_[0];
   for($i=1; $i<$#_; $i++)
   {
      if($_[$i] < $minimo) { $minimo = $_[$i]; }
   }
   return $minimo
}

#-----------------------------------------------------------------------------

1;
